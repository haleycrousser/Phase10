//the internal refill, checks if buffer has data. never called directly.
static ssize_t rio_read(rio_t *rp, char *usrbuf, size_t n) {
    while (rp->rio_cnt <= 0) {
#ifdef _WIN32
        rp->rio_cnt = recv(rp->rio_fd, rp->rio_buf, RIO_BUFSIZE, 0);
        if (rp->rio_cnt == SOCKET_ERROR) return -1;
#else
        rp->rio_cnt = (int)recv(rp->rio_fd, rp->rio_buf, RIO_BUFSIZE, 0);
        if (rp->rio_cnt < 0) return -1;
#endif
        if (rp->rio_cnt == 0) return 0;  // EOF
        rp->rio_bufptr = rp->rio_buf;
    }

    size_t cnt = (n < (size_t)rp->rio_cnt) ? n : (size_t)rp->rio_cnt;
    memcpy(usrbuf, rp->rio_bufptr, cnt);
    rp->rio_bufptr += cnt;
    rp->rio_cnt    -= (int)cnt;
    return (ssize_t)cnt;
}

//zeros everything out and links to socket.
void rio_readinitb(rio_t *rp, socket_t fd) {
    rp->rio_fd     = fd;
    rp->rio_cnt    = 0;
    rp->rio_bufptr = rp->rio_buf;
}

//reads one fill line from the socket, keeps going until new line.
//what server and client uses to recieve messages
ssize_t rio_readlineb(rio_t *rp, void *usrbuf, size_t maxlen) {
    char  c;
    char *bufp   = (char *)usrbuf;
    ssize_t n    = 0;
    ssize_t rc;

    for (size_t i = 1; i < maxlen; i++) {
        rc = rio_read(rp, &c, 1);
        if (rc == 1) {
            *bufp++ = c;
            n++;
            if (c == '\n') break;
        } else if (rc == 0) {
            break;
        } else {
            return -1;
        }
    }
    *bufp = '\0';
    return n;
}

//server only, creates a socket, binds it to a port number and starts listening.
ssize_t rio_writen(socket_t fd, const void *usrbuf, size_t n) {
    size_t      nleft = n;
    const char *bufp  = (const char *)usrbuf;

    while (nleft > 0) {
#ifdef _WIN32
        int nwritten = send(fd, bufp, (int)nleft, 0);
        if (nwritten == SOCKET_ERROR) return -1;
#else
        ssize_t nwritten = send(fd, bufp, nleft, 0);
        if (nwritten < 0) return -1;
#endif
        nleft -= (size_t)nwritten;
        bufp  += nwritten;
    }
    return (ssize_t)n;
}

