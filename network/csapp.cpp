//client only, looks up servers address and connects to it. returns socket ready to talk to server.
socket_t open_clientfd(const char *hostname, int port) {
    struct addrinfo hints, *result;
    memset(&hints, 0, sizeof(hints));
    hints.ai_family   = AF_INET;
    hints.ai_socktype = SOCK_STREAM;

    char portstr[8];
    snprintf(portstr, sizeof(portstr), "%d", port);

    if (getaddrinfo(hostname, portstr, &hints, &result) != 0) {
        fprintf(stderr, "getaddrinfo failed\n");
        return INVALID_SOCK;
    }

    socket_t clientfd = socket(result->ai_family,
                                result->ai_socktype,
                                result->ai_protocol);
    if (clientfd == INVALID_SOCK) {
        freeaddrinfo(result);
        perror("socket");
        return INVALID_SOCK;
    }

    if (connect(clientfd, result->ai_addr, (int)result->ai_addrlen) < 0) {
        freeaddrinfo(result);
        close_socket(clientfd);
        perror("connect");
        return INVALID_SOCK;
    }

    freeaddrinfo(result);
    return clientfd;
}
